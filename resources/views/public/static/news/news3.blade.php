        <h2 class="my-2 text-xl sm:text-3xl font-black">Pertama di Jatim, Urban Athletes Kenalkan Virtual Fitness Studio</h2>
        <div class="space-y-2 pt-2">
          <p class="text-sm sm:text-base">
            Surabaya, Bhirawa <br>
            Kesibukan masyarakat Surabaya yang kian padat, kerap menjadi alasan mereka untuk tidak bisa menjaga kebugaran sehari-hari. Kini kendala itu coba diatasi oleh Urban Athletes dengan mengenalkan virtual fitness studio pertama di Jatim, yakni kelas kebugaran tanpa instruktur langsung di lokasi.
          </p>
          <p class="text-sm sm:text-base">
            “Virtual fitness studio ini makin populer di negara lain, tapi di Jatim baru pertama ini kami kenalkan. Model ini cocok bagi warga yang memiliki kesibukan tinggi, tak bisa mengikuti jadwal kelas dengan instruktur yang sudah disediakan pusat kebugaran,” kata CEO Urban Athletes Verawati Budianto di sela trial pembukaan Urban Athletes di Marvell City Surabaya, Sabtu (25/11). <br>
            Dijelaskan Vera, dengan layar lebar member bisa mengikuti gerakan-gerakan fitness yang ditampilkan pada layar dengan teknologi visual. Untuk model fitness ini, Urban Athletes bekerjasama dengan LesMills, sebuah grup exercise provider dari Selandia Baru yang menjadwalkan kelas kebugaran update terbaru dengan pelatih tersertifikasi. “Karena virtual, maka kelas fitness ini bisa live dan tayang di beberapa negara, tak hanya di Surabaya. Jadwal kelas menyesuaikan dengan jadwal kelas yang ada di LesMills. Jadi ini fitness live juga, hanya instrukturnya tidak di Surabaya,” ujarnya. <br>
            Kelas fitness yang ada di virtual studio ada beragam jenis, mulai BodyPump, BodyCombat, BodyBalance, SH’BAM, RPM dan Sprint. Kelas ini dihadirkan untuk menyesuaikan dengan keinginan member, dan tubuh bagian mana yang mau dibentuk. <br>
            Kemarin model kebugaran baru ala virtual fitness studio itu dikenalkan secara langsung. Menampilkan layar besar, satu instruktur Urban Athletes terlihat serius mengikuti gerakan yang ditampilkan instruktur asing dalam layar film. <br>
            Hadirnya virtual studio ini diapresiasi positif sejumlah warga yang hadir. Seperti diakui Retno, warga Surabaya. Dia menilai virtual studio lebih menguntungkan bagi mereka yang sibuk daripada fitness konvensional. “Kalau yang konvensional, member menyesuaikan dengan jadwal yang telah disusun pusat kebugaran. Dengan virtual studio, tak ada batasan jadwal dan waktu lagi. Setiap saat kita mau fitness bisa dengan instruktur asing,” katanya. [tis] <br>
          </p>
          <p class="text-sm sm:text-base">
            “The Valor bertujuan meningkatkan kualitas hidup kita, dengan latihan intensitas tinggi. Dengan latihan ala kegiatan sehari- hari tapi dengan alat. Semisal dulu jalan aja ngos-ngosan sekarang bisa lari. Asalkan rutin – seminggu tiga kali – dalam sebulan sudah bagus dan terlihat perubahannya. ” tutur Verawati Budiyanto, owner dari Urban Athletes , Sabtu, (03/03/2018). 
          </p>
          <p class="text-sm sm:text-base">
            Urban Athletes Marvell City yang memiliki luas 600 meter persegi saat ini sudah memiliki 600 member dengan sebagian besar diantaranya adalah kaum hawa. Terdapat 3 studio serta virtual fitness studio. Selain The Valor, Urban Athletes juga memiliki kelas BodyPump, BodyBaIance, BodyCombat, RPM, dan Sprint yang terbagi dalam 500 kelas setiap minggunya. Kesemuanya berada di bawah lisensi resmi LesMills. Urban Athletes juga memiliki kelas hip hop khusus anak-anak yang diadakan seminggu sekali di semua cabang clubnya. 
          </p>
          <p class="text-sm sm:text-base">
            Pada saat grand opening Urban Athletes turut menghadirkan Eva Rais, penggiat ketogenic diet serta Kirsten Murphy, praktisi ketogenic asal Australia yang berbagi pengetahuan mengenai diet keto. Selain itu juga ada cooking class makanan-makanan sehat dengan Chef Hangga dan Chef Rinl. Centroone.com
          </p>
